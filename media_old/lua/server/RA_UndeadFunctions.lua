PlayerZed = {}

local function OnGameStart()
	if getPlayer() then
		local pl = getPlayer()
		if not pl:getModData()['isUndead'] == nil then pl:getModData()['isUndead'] = false end
		if not pl:getModData()['isUndead'] == nil then pl:getModData()['isUndead'] = false end
	end
end
Events.OnGameStart.Add(OnGameStart)



