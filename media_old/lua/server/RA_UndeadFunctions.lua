PlayerZed.time = PlayerZed.time or 0

function PlayerZed.Clock()
	if PlayerZed.time == 149 then
		PlayerZed.time = 0
	else
		PlayerZed.time = PlayerZed.time + 1
	end
end

function PlayerZed.ResetStats(pl)
	if pl and pl:getModData()['isUndead'] then
		local Stats = pl:getStats()
		Stats:setAnger(0.0)
		Stats:setBoredom(0.0)
		Stats:setEndurance(1.0)
		Stats:setEndurancelast(1.0)
		Stats:setEndurancedanger(0.25)
		Stats:setEndurancewarn(0.5)
		Stats:setFatigue(0.0)
		Stats:setFitness(0.0)
		Stats:setHunger(0.0)
		Stats:setIdleboredom(0.0)
		Stats:setMorale(1.0)
		Stats:setStress(0.0)
		Stats:setStressFromCigarettes(0.0)
		Stats:setFear(0.0)
		Stats:setPanic(0.0)
		Stats:setSanity(1.0)
		Stats:setSickness(0.0)
		Stats:setPain(0.0)
		Stats:setDrunkenness(0.0)
		Stats:setNumVisibleZombies(0)
		Stats:setTripping(false)
		Stats:setTrippingRotAngle(0.0)
		Stats:setThirst(0.0)
	end
end




